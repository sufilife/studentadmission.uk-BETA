        <p>This week, Johns Hopkins will resume requiring test scores from undergraduate applicants, MIT released its first demographic data since the Supreme Court’s decision to end affirmative action, Vanderbilt confirmed plans to build a Florida campus, and colleges face growing demands to discipline student protestors.</p>
        <p>Johns Hopkins University will resume <a href="https://hub.jhu.edu/2024/08/16/undergrad-admissions-standardized-test-requirement/" target="_blank" rel="noopener noreferrer">requiring ACT or SAT scores</a> from undergraduate applicants after adopting a temporary test-optional policy in 2020 due to Covid-19. The requirement will take effect next year and will apply to students seeking admission to the Krieger School of Arts and Sciences and the Whiting School of Engineering. Students applying to enter Hopkins in fall 2025 are “encouraged” to include test scores in their applications.</p>
        <p>MIT released its first demographic data for the incoming Class of 2028, following the Supreme Court’s decision to end affirmative action in college admissions. <a href="https://www.boston.com/news/local-news/2024/08/21/mit-reports-less-diverse-incoming-class-due-to-the-end-of-affirmative-action/" target="_blank" rel="noopener noreferrer">The data</a> reveals drops in the enrollment of Black, Hispanic, Native American, and Pacific Islander students.</p>
